	// Start ADC with DMA
	HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adcBuf, 12);
	
	// Variables for timing
	uint32_t last_adc_update = 0;
	const uint32_t ADC_UPDATE_INTERVAL = 500; // 500ms interval for ADC updates
	
